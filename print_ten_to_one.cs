using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
//Print Numbers from 10 to 1
namespace _404print_ten_to_one
{
    class Program
    {
        static void Main(string[] args)
        {
            int i;
            for(i=10;i>=1;i--)
            {
                Console.WriteLine(i);
            }
            Console.ReadLine();
        }
    }
}
